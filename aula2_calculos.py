# Arquivo para aprender a realizar calculos com python

# Sinais matemáticos :
# + Soma                        Serve para somar valores
# - Subtração                   Serve para diminuir um valor do outro
# * Multiplicação               Serve para multiplicar valores
# / Divisão                     Serve para dividir um valor do outro
# ** Potência                   Serve para fazer a potência de valores
# // Divisão inteira            Serve para retornar a parte inteira da divisão
# % Resto de divisão            Serve para retormar o resto da divisão

numero_1 = int(input("Digite o primeiro valor: "))
numero_2 = int(input("Digite o segundo valor: "))

resultado = numero_1 + numero_2
print("A soma do numero_1 com o numero_2 é: ",resultado)

resultado = numero_1 - numero_2
print("A subtração do numero_1 pelo numero_2 é: ",resultado)

resultado = numero_1 * numero_2
print("A multiplicação do numero_1 pelo numero_2 é: ",resultado)

resultado = numero_1 / numero_2
print("A divisão do numero_1 pelo numero_2 é: ",resultado)

resultado = numero_1 ** numero_2
print("A potência do numero_1 pelo numero_2 é: ",resultado)

resultado = numero_1 // numero_2
print("A Divisão inteira do numero_1 pelo numero_2 é: ",resultado)

resultado = numero_1 % numero_2
print("O resto da divisão do numero_1 pelo numero_2 é: ",resultado)
